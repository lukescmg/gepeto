package Model;

public class Usuario {
	String UsuarioEmail;
	String UsuarioNome;
	String UsuarioSobrenome;
	String UsuarioDataNascimento;
	String UsuarioTelefone;
	String UsuarioSenha;
	
	public String getUsuarioEmail() {
		return UsuarioEmail;
	}
	public void setUsuarioEmail(String UsuarioEmail) {
		this.UsuarioEmail = UsuarioEmail;
	}
	public String getUsuarioNome() {
		return UsuarioNome;
	}
	public void setUsuarioNome(String UsuarioNome) {
		this.UsuarioNome = UsuarioNome;
	}
	public String getUsuarioSobrenome() {
		return UsuarioSobrenome;
	}
	public void setUsuarioSobrenome(String UsuarioSobrenome) {
		this.UsuarioSobrenome = UsuarioSobrenome;
	}
	public String getUsuarioDataNascimento() {
		return UsuarioDataNascimento;
	}
	public void setUsuarioDataNascimento(String UsuarioDataNascimento) {
		this.UsuarioDataNascimento = UsuarioDataNascimento;
	}
	public String getUsuarioTelefone() {
		return UsuarioTelefone;
	}
	public void setUsuarioTelefone(String UsuarioTelefone) {
		this.UsuarioTelefone = UsuarioTelefone;
	}
	public String getUsuarioSenha() {
		return UsuarioSenha;
	}
	public void setUsuarioSenha(String UsuarioSenha) {
		this.UsuarioSenha = UsuarioSenha;
	}
}
